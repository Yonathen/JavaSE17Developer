public class PrePostIncDec {
	public static void main(String[] args) {
		int parkAttendance = 0;
		System.out.println(parkAttendance); // prints 0
		System.out.println(++parkAttendance); // prints 1
		System.out.println(parkAttendance); // prints 1
		System.out.println(parkAttendance--); // Prints 1
		System.out.println(parkAttendance); // Prints 0
	}
}

